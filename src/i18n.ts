export type Language = "en" | "pl"

export const SUPPORTED_LANGUAGES: Language[] = ["en", "pl"]
export const UI_LANGUAGE_STORAGE_KEY = "noisecut.uiLanguage"
export const DEFAULT_REPORT_LANGUAGE_STORAGE_KEY = "noisecut.defaultReportLanguage"

export function isSupportedLanguage(value: string | null | undefined): value is Language {
  return value === "en" || value === "pl"
}

export function normalizeLanguage(value: string | null | undefined, fallback: Language = "en"): Language {
  if (!value) {
    return fallback
  }

  const normalized = value.toLowerCase().slice(0, 2)
  return isSupportedLanguage(normalized) ? normalized : fallback
}

export function detectInitialLanguage(): Language {
  const stored = window.localStorage.getItem(UI_LANGUAGE_STORAGE_KEY)
  if (isSupportedLanguage(stored)) {
    return stored
  }

  return normalizeLanguage(window.navigator.language, "en")
}

export function languageName(language: Language, uiLanguage: Language = language) {
  if (uiLanguage === "pl") {
    return language === "pl" ? "Polski" : "Angielski"
  }

  return language === "pl" ? "Polish" : "English"
}

